#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t	index;
	size_t	src_size;

	index = 0;
	src_size = ft_strlen(src);
	if (!dest || !src)
		return (0);
	if (size == 0)
		return (src_size);
	while (src[index] && index < size - 1)
	{
		dest[index] = src[index];
		index++;
	}
	dest[index] = '\0';
	return (src_size);
}
